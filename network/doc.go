package network

/*
=======================================================================
  Docker 网络原理详解
=======================================================================

  Docker 网络的核心：Network Namespace + veth pair + Bridge + NAT

  ┌─────────────────────────────────────────────────────────────┐
  │  宿主机 Network Namespace                                   │
  │  ┌─────────────────────────────────────────────────────┐   │
  │  │  eth0 (192.168.1.100)                               │   │
  │  │     │                                               │   │
  │  │  docker0 (172.17.0.1) ← Bridge                     │   │
  │  │     ├── veth-a ← 容器A 的 veth 一端                 │   │
  │  │     └── veth-b ← 容器B 的 veth 一端                 │   │
  │  └─────────────────────────────────────────────────────┘   │
  │                                                             │
  │  ┌──────────────────────┐  ┌──────────────────────┐        │
  │  │ 容器A Network NS     │  │ 容器B Network NS     │        │
  │  │ eth0 (172.17.0.2)   │  │ eth0 (172.17.0.3)   │        │
  │  │ veth-a 的另一端      │  │ veth-b 的另一端      │        │
  │  └──────────────────────┘  └──────────────────────┘        │
  └─────────────────────────────────────────────────────────────┘

  容器间通信流程：
  容器A → 容器A.eth0 → veth-a → docker0 → veth-b → 容器B.eth0 → 容器B

  容器访问外网流程：
  容器 → 容器.eth0 → veth → docker0 → NAT(iptables) → 宿主机.eth0 → 外网

  ┌─────────────────────────────────────────────────────────────┐
  │  veth pair（虚拟以太网设备对）                               │
  │                                                             │
  │  特点：                                                     │
  │  - 总是成对创建，像一根虚拟网线                              │
  │  - 一端连接容器，一端连接宿主机网桥                          │
  │  - 数据从一端进，从另一端出                                  │
  │  - 删除一端，另一端也会消失                                  │
  │                                                             │
  │  创建命令：                                                 │
  │  ip link add veth-host type veth peer name veth-container  │
  └─────────────────────────────────────────────────────────────┘

  ┌─────────────────────────────────────────────────────────────┐
  │  Bridge（网桥）                                             │
  │                                                             │
  │  作用：                                                     │
  │  - 连接多个网络设备（类似交换机）                            │
  │  - Docker 默认创建 docker0 网桥                             │
  │  - 所有容器的 veth 一端都连接到网桥                          │
  │  - 容器之间通过网桥直接通信                                  │
  │                                                             │
  │  创建命令：                                                 │
  │  ip link add docker0 type bridge                           │
  │  ip addr add 172.17.0.1/16 dev docker0                    │
  │  ip link set docker0 up                                    │
  └─────────────────────────────────────────────────────────────┘

  ┌─────────────────────────────────────────────────────────────┐
  │  NAT（网络地址转换）                                        │
  │                                                             │
  │  作用：让容器可以访问外网                                    │
  │  原理：将容器的私有 IP 转换为宿主机的公网 IP                 │
  │                                                             │
  │  iptables -t nat -A POSTROUTING \                          │
  │    -s 172.17.0.0/16 ! -o docker0 -j MASQUERADE             │
  │                                                             │
  │  端口映射（docker run -p 8080:80）：                        │
  │  iptables -t nat -A PREROUTING \                           │
  │    -p tcp --dport 8080 -j DNAT --to-destination 172.17.0.2:80│
  └─────────────────────────────────────────────────────────────┘

  Docker 网络模式对比：
  ┌──────────────┬─────────────────────────────────────────────┐
  │ bridge       │ 默认模式，veth + bridge + NAT               │
  │ host         │ 共享宿主机网络，无隔离，性能最好             │
  │ none         │ 无网络，只有 lo 接口                        │
  │ container:id │ 共享另一个容器的网络栈                      │
  │ overlay      │ 跨主机通信，VXLAN 隧道                      │
  │ macvlan      │ 容器拥有独立 MAC，直接接入物理网络          │
  └──────────────┴─────────────────────────────────────────────┘

=======================================================================
*/
