package cgroup

/*
=======================================================================
  Cgroup 原理详解
=======================================================================

  Cgroup（Control Group）是 Linux 内核提供的一种机制，
  用于将进程分组并对每组进程进行资源限制和监控。

  ┌─────────────────────────────────────────────────────────────┐
  │  Cgroup 的核心概念                                          │
  ├─────────────────────────────────────────────────────────────┤
  │                                                             │
  │  hierarchy（层级树）                                        │
  │  ├── cgroup1/                                               │
  │  │   ├── cgroup.procs  ← 属于该组的进程 PID 列表           │
  │  │   ├── memory.limit_in_bytes  ← 内存限制                 │
  │  │   └── cpu.shares  ← CPU 份额                            │
  │  ├── cgroup2/                                               │
  │  │   └── ...                                                │
  │  └── ...                                                    │
  │                                                             │
  │  子 cgroup 继承父 cgroup 的限制                             │
  │  子 cgroup 可以设置更严格的限制                              │
  └─────────────────────────────────────────────────────────────┘

  Cgroup v1 vs v2:
  ┌──────────────┬─────────────────────────────────────────────┐
  │ Cgroup v1    │ 每个子系统独立挂载                           │
  │              │ /sys/fs/cgroup/memory/                      │
  │              │ /sys/fs/cgroup/cpu/                         │
  │              │ /sys/fs/cgroup/blkio/                       │
  │              │ 灵活但管理复杂                               │
  ├──────────────┼─────────────────────────────────────────────┤
  │ Cgroup v2    │ 统一层级                                    │
  │              │ /sys/fs/cgroup/                             │
  │              │ 更简洁，Docker 新版本默认使用                │
  └──────────────┴─────────────────────────────────────────────┘

  Docker 使用 Cgroup 的方式：
  1. 每个容器创建一个 cgroup：/sys/fs/cgroup/docker/<container-id>/
  2. 将容器进程 PID 写入 cgroup.procs
  3. 根据用户参数设置资源限制

  docker run -m 100m → echo 104857600 > memory.limit_in_bytes
  docker run --cpus=1 → echo 100000 > cpu.cfs_quota_us
  docker run --cpu-shares=512 → echo 512 > cpu.shares

=======================================================================
*/
