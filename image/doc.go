/*
=======================================================================
  Docker 镜像原理详解
=======================================================================

  Docker 镜像的核心创新：分层（Layer）+ 联合文件系统（UnionFS）

  ┌─────────────────────────────────────────────────────────────┐
  │  传统虚拟机镜像                                              │
  │  ┌───────────────────────────────────────┐                 │
  │  │  一个完整的磁盘镜像（10GB+）            │                 │
  │  │  包含：OS + 依赖 + 应用               │                 │
  │  │  问题：体积大、传输慢、无法共享         │                 │
  │  └───────────────────────────────────────┘                 │
  ├─────────────────────────────────────────────────────────────┤
  │  Docker 镜像                                                │
  │  ┌─────────────────┐  ← 可写层（容器运行时修改）            │
  │  ├─────────────────┤  ← 层3: RUN pip install flask         │
  │  ├─────────────────┤  ← 层2: COPY app.py /app/             │
  │  ├─────────────────┤  ← 层1: FROM python:3.9               │
  │  └─────────────────┘                                        │
  │  每层只存储差异（增量），可共享、可缓存                      │
  └─────────────────────────────────────────────────────────────┘

  OverlayFS 工作原理（Docker 默认使用的 UnionFS）：

  ┌─────────────────────────────────────────────────────────────┐
  │  merged/  ← 用户看到的统一视图                               │
  │  ├── upper/  ← 可写层（容器修改写到这里）                    │
  │  ├── lower1/ ← 只读层1                                      │
  │  ├── lower2/ ← 只读层2                                      │
  │  └── work/  ← OverlayFS 工作目录                            │
  │                                                             │
  │  读取：从上到下查找，找到即返回                              │
  │  修改：Copy-on-Write，复制到 upper 层再修改                  │
  │  删除：在 upper 层创建 whiteout 文件标记删除                 │
  └─────────────────────────────────────────────────────────────┘

  Dockerfile 指令与层的对应关系：
  FROM ubuntu:22.04     → 基础层
  RUN apt-get update    → 新增一层（包含更新后的文件）
  COPY . /app           → 新增一层（包含复制的文件）
  RUN pip install -r requirements.txt → 新增一层
  CMD ["python", "app.py"] → 元数据（不增加层）

  镜像存储位置：
  /var/lib/docker/overlay2/
  ├── <layer-id-1>/
  │   └── diff/    ← 该层的文件
  ├── <layer-id-2>/
  │   └── diff/
  └── <merged-id>/
      ├── merged/  ← 叠加后的完整文件系统
      ├── upper/   ← 容器可写层
      └── work/    ← 工作目录

  镜像 Registry 交互（docker pull/push）：
  1. 客户端 → Registry: GET /v2/<name>/manifests/<tag>
  2. Registry → 客户端: 返回 manifest（层列表）
  3. 客户端逐层下载: GET /v2/<name>/blobs/<digest>
  4. 验证 SHA256，解压，存储

=======================================================================
*/
